def addition (num1,num2):
    return num1 + num2
def exponential (num1, numb2):
    return num1 ** numb2
def subtraction (num1, num2):
    return num1 - num2
def multiplication (num1, num2):
    return num1*num2
def square_root(num1):
    return num1**(1/2)
def division (num1, num2):
    return num1/num2